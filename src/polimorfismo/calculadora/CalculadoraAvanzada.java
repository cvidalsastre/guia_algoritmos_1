package polimorfismo.calculadora;

public class CalculadoraAvanzada {
public static  Number sumar(int a, int b){
    return a + b ;

}; // para sumar dos números enteros.
public static Number sumar(double a, double b){
    return a + b;

} ;// para sumar dos números de punto flotante.
public static Number restar(int a, int b){
    return a-b;

};// para restar dos números enteros.
public static Number restar(double a, double b) {
    return a - b;

};//para restar dos números de punto flotante.
public static Number multiplicar(int a, int b){
    return a * b ;

};// para multiplicar dos números enteros.
public static Number multiplicar(double a, double b){
    return a * b;

};// para multiplicar dos números de punto flotante.
public static Number dividir(int a, int b){
    return a / b ;

}; //para dividir dos números enteros.
public static Number dividir(double a, double b){

    return a / b ;

};// para dividir dos números de punto flotante.
}
